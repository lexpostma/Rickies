<h1>The Bill of Rickies</h1>

<h2>The Rickies</h2>
<ol>
	<li>There are two types of Rickies: Annual Rickies and Keynote Rickies.</li>
	<li>The winner of the Annual Rickies is named the Annual Chairman, and retains the rights to the corresponding Twitter account for the full year. This position is awarded every January.</li>
	<li>The Keynote Rickies winner is named the Keynote Chairman, and retains the rights to the corresponding Twitter account until the next keynote is held.</li>
	<li>Annual winners roll over to preserve the order of picks. For example the winner of the 2019 annual winner gets to go first for the next annual picks.</li>
	<li>Order for the Keynote Rickies is based on the previous Apple event. </li>
	<li>The loser goes last.</li>
	<li>To earn any points, everything written down in the prediction document must come true.</li>
	<li>No half points may be awarded in any round and picks can not be reused.</li>
	<li>1 point is awarded for any pick deemed correct in the first 2 rounds.</li>
	<li>2 points will be awarded for correct picks in the Risky Pick round.</li>
	<li>If your Risky Pick is wrong, you lose a point.</li>
	<li>And the two other hosts must agree that your pick is “risky”.</li>
	<li>For Keynote Rickies the scoring window starts when the event begins and closes when the picks are scored.</li>
	<li>The winner of the regular and Risky Picks must be granted access to the Annual or Event Chairman Twitter account and will retain access until another winner is named.</li>
</ol>

<h2>The Flexies</h2>
<ol>
	<li>Loser of the Flexies must compensate the winner of the Flexies by donating to a charity of the winner’s choice.</li>
	<li>The amount of the donation is $25 per wrong Flexy made by the loser.</li>
	<li>In the case of a tie, the ratio of correct to incorrect Flexies will be taken into account.</li>
	<li>Each host must make a minimum of 5 Flexy Picks.</li>
	<li>Flexies may be reused as Flexies or regular picks.</li>
	<li>The money, of course, much be donated on air.</li>
</ol>

